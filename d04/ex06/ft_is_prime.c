int	ft_is_prime(int nb)
{
	int i;

	if (nb <= 1)
		return (0);
	if (nb % 2 == 0 && nb != 2)
		return (0);
	i = 3;
	while (i <= 46340 && i < nb)
		if (nb % i == 0)
			return (0);
		else
			i += 2;
	return (1);
}
